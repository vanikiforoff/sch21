#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	size_t	i;

	if (!s)
		return (NULL);
	if (!(str = ft_strnew(ft_strlen(s))))
		return (NULL);
	i = 0;
	while (i < ft_strlen(s))
	{
		*(str + i) = f(*(s + i));
		i++;
	}
	return (str);
}
