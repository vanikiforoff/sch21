#include "libft.h"

void	ft_bzero(void *s, size_t count)
{
	size_t	i;
	char	*tmp;

	tmp = (char *)s;
	i = 0;
	while (i < count)
	{
		*(tmp + i) = '\0';
		i++;
	}
}
