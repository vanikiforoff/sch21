char	*ft_strrev(char *str)
{
	char	c;
	int		i;
	int		j;

	i = 0;
	while (*(str + i) != '\0')
	{
		i++;
	}
	j = 0;
	while (j < (i / 2))
	{
		c = *(str + j);
		*(str + j) = *(str + i - j - 1);
		*(str + i - j - 1) = c;
		j++;
	}
	return (str);
}
