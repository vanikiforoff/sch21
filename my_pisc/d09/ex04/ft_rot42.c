char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (*(str + i) != '\0')
	{
		if ((*(str + i) >= 'A') && (*(str + i) <= 'Z'))
			*(str + i) = 'A' + (*(str + i) - 'A' + 42) % 26;
		if ((*(str + i) >= 'a') && (*(str + i) <= 'z'))
			*(str + i) = 'a' + (*(str + i) - 'a' + 42) % 26;
		i++;
	}
	return (str);
}
