char	*ft_strupcase(char *str)
{
	int d;
	int i;

	d = 'a' - 'A';
	i = 0;
	while (*(str + i) != '\0')
	{
		if ((*(str + i) >= 'a') && (*(str + i) <= 'z'))
			*(str + i) = *(str + i) - d;
		i++;
	}
	return (str);
}
